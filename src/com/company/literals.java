package com.company;

public class literals {
    public static void main(String[] args) {
        byte age = 10;
        short age1 = 30;  // by default byte, short, int, takes itself
        int age2 = 50;
        long age3 = 3844998; // by default it takes integer so we need to mention l or L.
        float number = 98.8434f; // by default it takes double so we need to mention f or F.
        double num = 123489.43989;
        char c = 'A'; // always character should be in single quotes.
        boolean a= true;
        String str = "Dilip"; // it is also a  literal in Java.
        System.out.println(str);
        System.out.println(age1);
    }
}
